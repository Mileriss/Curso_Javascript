/*
Os operadores aritméticos/matematicos são utilizados para realizar operações entre variaveis, numeros, string e etc...

+  Adição
- Vai realizar a soma da operação

-  Subtração
- Vai realizar a subtração na operação

* Multiplicação
- Vai realizar a multiplicação da operação

/ Divisão
- Vai dividir o valor entre os elementos

% Módulo/Resto da divisão
- Vai retornar o valor que sobra da divisão entre os elementos

++ Incremento
- Vai adicionar o valor 1 ao resultado da operação

-- Decremento
- Vai retirar o valor 1 do resultado da operação

+= Mais igual
- Vai pegar o valor da variavel e realizar a soma do valor da variavel mais o valor que for definido

-+ Menos igual
- Vai pegar o valor da variavel e realizar a subtração do valor da variavel menos o valor que for definido
*/

var n1 = 5
var n2 = 10

//ADIÇÃO
console.log("ADIÇÃO")
resultado_adicao = n1+n2
console.log(n1+n2)
console.log(resultado_adicao)

//SUBTRAÇÃO
console.log("SUBTRAÇÃO")
resultado_subtracao = n1-n2
console.log(n1-n2)
console.log(resultado_subtracao)

//MULTIPLICAÇÃO
console.log("MULTIPLICAÇÃO")
resultado_multiplicacao = n1*n2
console.log(n1*n2)
console.log(resultado_multiplicacao)

//DIVISÃO
console.log("DIVISÃO")
resultado_divisao = n1/n2
console.log(n1/n2)
console.log(resultado_divisao)

//MÓDULO/RESTO DA DIVISÃO
console.log("MÓDULO/RESTO DA DIVISÃO")
resultado_modulo = n1%n2
console.log(n1%n2)
console.log(resultado_modulo)

//INCREMENTO
console.log("INCREMENTO")
console.log(++n1)
console.log(++n2)

//DECREMENTO
console.log("DECREMENTO")
var num1 = 5
num1--
console.log(num1)
var num2 = 10
num2--
console.log(num2)

//MAIS IGUAL
console.log("MAIS IGUAL")
console.log(n1+=1)

//MENOS IGUAL
console.log("MENOS IGUAL")
console.log(n1+=1)