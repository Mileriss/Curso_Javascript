/*
- Com a operação ternária é possível reduzir ou simplicar estruturas de comparação como o IF ELSE IF

SINTAXE:
variavel = (<condição> ? <"True"> : <"False">)
? - É o simbolo que vai indicar que a operação será ternária
*/

var num = 10

// Operação básica
// - Caso a variavel 'num' seja maior que o valor 10, entra na condição entre verdadeiro e falso
resultado = ((num > 10) ? "Verdadeiro" : "Falso")
console.log(resultado)