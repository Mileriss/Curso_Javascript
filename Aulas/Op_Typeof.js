/*
- Typeof é um operador que vai retornar o tipo de uma variável
*/

var num_int = 10
var num_float = 10.5
var texto = "10"
var booleana = false

//Exibindo o tipo de dado
console.log(typeof(num_int))
console.log(typeof(num_float))
console.log(typeof(texto))
console.log(typeof(booleana))

console.log("\n")

//Exibindo o valor e o tipo
console.log("Valor: " + num_int + " - " + "Tipo: " + typeof(num_int))
console.log("Valor: " + num_float + " - " + "Tipo: " + typeof(num_float))
console.log("Valor: " + texto + " - " + "Tipo: " + typeof(texto))
console.log("Valor: " + booleana + " - " + "Tipo: " + typeof(booleana))