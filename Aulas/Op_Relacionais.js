/*
- Operadores Relacionais são os operadores de comparação e validação entre elementos/variáveis de um código
- Os operadores vão retornar respostas Booleanas e realizar determinada ação de acordo com a resposta retornada
- Geralmente são utilizados em operações para realizar comparações, por exemplo o IF, ELSE IF, SWITCH CASE e etc...

> Maior
- Operador que vai verificar se um elemento é maior que outro

< Menor
- Operador que vai verificar se um elemento é menor que outro

>= Maior Igual
- Operador que vai verificar se um elemento é maior ou igual a outro

<= Menor Igual
- Operador que vai verificar se um elemento é menor ou igual a outro

== Igual
- Operador que vai realizar a comparação e verificar se um elemento é igual ao outro

!= Diferente
- Operador que vai verificar se um elemento é diferente de outro
*/

var n1 = 10
var n2 = 5

//MAIOR
if (n1>n2){
    console.log("N1 é maior que N2")
}

//MENOR
if (n1<n2){
    console.log("N1 é menor que N2")
}

//MAIOR IGUAL
if (n1>=n2){
    console.log("N1 é maior ou igual a N2")
}

//MENOR IGUAL
if (n1<=n2){
    console.log("N1 é menor ou igual a N2")
}

//IGUAL
if (n1==n2){
    console.log("N1 é igual a N2")
}

//DIFERENTE
if (n1!=n2){
    console.log("N1 é diferente a N2")
}